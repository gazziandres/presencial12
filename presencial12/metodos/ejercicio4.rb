# Crear un método que muestre en pantalla un saludo, el método
# debe recibir un parámetro, si ese parámetro es el string "Hola" el
# método debe mostrar en pantalla "Hola Mundo".
def hola (string)
 if string == 'Hola' 
 	puts 'Hola Mundo' 
else
  	puts 'Yo no entender español'
end
end


